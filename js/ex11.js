var n2, n2, op, r
n1 = parseInt(prompt("Digite um número"))
n2 = parseInt(prompt("Digite um número"))
op = prompt("Digite a operação: +, -, *, /")

if (op == "+") {
    alert(r = n1 + n2)
}
if (op == "-") {
    alert(r = n1 - n2)
}
if (op == "*") {
    alert(r = n1 * n2)
}
if (op == "/") {
    alert(r = n1 / n2)
}

