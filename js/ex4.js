var num
num = parseInt(prompt("Digite um número: "))

if (num == 0) {
    alert(num + " = " + "zero")
}
else if (num == 1) {
    alert(num + " = " + "um")
}
else if (num == 2) {
    alert(num + " = " + "dois")
}
else if (num == 3) {
    alert(num + " = " + "três")
}
else if (num == 4) {
    alert(num + " = " + "quatro")
}
else if (num == 5) {
    alert(num + " = " + "cinco")
}
else if (num == 6) {
    alert(num + " = " + "seis")
}
else if (num == 7) {
    alert(num + " = " + "sete")
}
else if (num == 8) {
    alert(num + " = " + "oito")
}
else if (num == 9) {
    alert(num + " = " + "nove")
}